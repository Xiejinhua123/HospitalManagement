package cn.vote.model;

public class UserLableModel {
	
	private Integer voteNumber;
	
	private Integer lableVoteNumber;
	
	private String usersID;
	
	private String url;
	
	private String name;

	public Integer getVoteNumber() {
		return voteNumber;
	}

	public void setVoteNumber(Integer voteNumber) {
		this.voteNumber = voteNumber;
	}

	public Integer getLableVoteNumber() {
		return lableVoteNumber;
	}

	public void setLableVoteNumber(Integer lableVoteNumber) {
		this.lableVoteNumber = lableVoteNumber;
	}

	public String getUsersID() {
		return usersID;
	}

	public void setUsersID(String usersID) {
		this.usersID = usersID;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
